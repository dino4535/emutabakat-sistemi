import { useState } from 'react'
import { useNavigate } from 'react-router-dom'
import { useMutation, useQuery } from '@tanstack/react-query'
import axios from 'axios'
import { toast } from 'react-toastify'
import { FaSave, FaFileInvoice, FaTimesCircle, FaUsers } from 'react-icons/fa'
import './MutabakatCreate.css'

export default function MutabakatCreateManual() {
  const navigate = useNavigate()
  
  const [formData, setFormData] = useState({
    receiver_id: '',
    donem_baslangic: '',
    donem_bitis: '',
    bakiye: '',
    bakiye_turu: 'alacak', // 'alacak' veya 'borc'
    aciklama: ''
  })

  // M√º≈üteri listesini √ßek (MUSTERI rol√ºndeki kullanƒ±cƒ±lar)
  const { data: customers, isLoading: customersLoading } = useQuery({
    queryKey: ['customers'],
    queryFn: async () => {
      const response = await axios.get('/api/bulk-mutabakat/customers')
      return response.data
    },
    onError: (error) => {
      console.error('M√º≈üteri listesi alƒ±namadƒ±:', error)
    }
  })

  // Mutabakat olu≈ütur
  const createMutation = useMutation({
    mutationFn: async (data) => {
      // Bakiye t√ºr√ºne g√∂re bor√ß/alacak hesapla
      const bakiye_value = parseFloat(data.bakiye) || 0
      let toplam_borc = 0
      let toplam_alacak = 0

      if (data.bakiye_turu === 'alacak') {
        toplam_alacak = bakiye_value
      } else {
        toplam_borc = bakiye_value
      }

      const payload = {
        receiver_id: parseInt(data.receiver_id),
        donem_baslangic: new Date(data.donem_baslangic).toISOString(),
        donem_bitis: new Date(data.donem_bitis + 'T23:59:59').toISOString(),
        toplam_borc: toplam_borc,
        toplam_alacak: toplam_alacak,
        aciklama: data.aciklama || `${data.donem_baslangic} - ${data.donem_bitis} D√∂nemi Bakiye Mutabakatƒ±`
      }

      console.log('Mutabakat olu≈üturma payload:', payload)
      const response = await axios.post('/api/mutabakat/', payload)
      return response.data
    },
    onSuccess: (data) => {
      toast.success('Mutabakat ba≈üarƒ±yla olu≈üturuldu! (Taslak)')
      navigate('/mutabakat')
    },
    onError: (error) => {
      console.error('Mutabakat olu≈üturma hatasƒ±:', error)
      const errorMsg = error.response?.data?.detail || error.message || 'Mutabakat olu≈üturulamadƒ±'
      toast.error(errorMsg)
    }
  })

  const handleSubmit = (e) => {
    e.preventDefault()

    // Validasyon
    if (!formData.receiver_id) {
      toast.error('L√ºtfen m√º≈üteri se√ßin')
      return
    }
    if (!formData.donem_baslangic || !formData.donem_bitis) {
      toast.error('L√ºtfen d√∂nem tarihlerini girin')
      return
    }
    if (!formData.bakiye || parseFloat(formData.bakiye) <= 0) {
      toast.error('L√ºtfen ge√ßerli bir bakiye girin')
      return
    }

    // Tarih kontrol√º
    if (new Date(formData.donem_baslangic) > new Date(formData.donem_bitis)) {
      toast.error('D√∂nem ba≈ülangƒ±√ß tarihi, biti≈ü tarihinden sonra olamaz')
      return
    }

    createMutation.mutate(formData)
  }

  const handleInputChange = (e) => {
    const { name, value } = e.target
    setFormData(prev => ({
      ...prev,
      [name]: value
    }))
  }

  const handleCancel = () => {
    if (window.confirm('ƒ∞ptal etmek istediƒüinizden emin misiniz? Girdiƒüiniz veriler kaybolacak.')) {
      navigate('/mutabakat')
    }
  }

  // Bug√ºn√ºn tarihini YYYY-MM-DD formatƒ±nda al
  const getTodayDate = () => {
    const today = new Date()
    return today.toISOString().split('T')[0]
  }

  // √ñnceki ayƒ±n ilk g√ºn√ºn√º al
  const getLastMonthFirstDay = () => {
    const date = new Date()
    date.setMonth(date.getMonth() - 1)
    date.setDate(1)
    return date.toISOString().split('T')[0]
  }

  // √ñnceki ayƒ±n son g√ºn√ºn√º al
  const getLastMonthLastDay = () => {
    const date = new Date()
    date.setDate(0) // Bir √∂nceki ayƒ±n son g√ºn√º
    return date.toISOString().split('T')[0]
  }

  return (
    <div className="mutabakat-create">
      <div className="page-header">
        <h1><FaFileInvoice /> Manuel Mutabakat Olu≈ütur</h1>
        <p>Bakiye mutabakatƒ± olu≈üturmak i√ßin t√ºm bilgileri manuel olarak girin</p>
      </div>

      <div className="create-form-container">
        <form onSubmit={handleSubmit} className="mutabakat-form">
          {/* M√º≈üteri Se√ßimi */}
          <div className="form-section">
            <h3><FaUsers /> 1. M√º≈üteri Se√ßimi</h3>
            <div className="form-group">
              <label className="required">M√º≈üteri</label>
              <select
                name="receiver_id"
                value={formData.receiver_id}
                onChange={handleInputChange}
                required
                disabled={customersLoading}
                className="form-select"
              >
                <option value="">M√º≈üteri se√ßiniz...</option>
                {customers && customers.map(customer => (
                  <option key={customer.id} value={customer.id}>
                    {customer.company_name || customer.full_name} 
                    {customer.vkn_tckn ? ` (${customer.vkn_tckn})` : ''}
                  </option>
                ))}
              </select>
              {customersLoading && <small className="text-muted">M√º≈üteriler y√ºkleniyor...</small>}
              {customers && customers.length === 0 && (
                <small className="text-warning">‚ö†Ô∏è Hi√ß m√º≈üteri bulunamadƒ±</small>
              )}
            </div>
          </div>

          {/* D√∂nem Bilgileri */}
          <div className="form-section">
            <h3>2. D√∂nem Bilgileri</h3>
            <div className="form-row">
              <div className="form-group">
                <label className="required">D√∂nem Ba≈ülangƒ±√ß</label>
                <input
                  type="date"
                  name="donem_baslangic"
                  value={formData.donem_baslangic}
                  onChange={handleInputChange}
                  max={getTodayDate()}
                  required
                  className="form-input"
                />
              </div>
              <div className="form-group">
                <label className="required">D√∂nem Biti≈ü</label>
                <input
                  type="date"
                  name="donem_bitis"
                  value={formData.donem_bitis}
                  onChange={handleInputChange}
                  max={getTodayDate()}
                  min={formData.donem_baslangic}
                  required
                  className="form-input"
                />
              </div>
            </div>
            <div className="quick-date-buttons">
              <button
                type="button"
                className="btn-quick-date"
                onClick={() => {
                  setFormData(prev => ({
                    ...prev,
                    donem_baslangic: getLastMonthFirstDay(),
                    donem_bitis: getLastMonthLastDay()
                  }))
                  toast.info('Ge√ßen ay d√∂nemi se√ßildi')
                }}
              >
                üìÖ Ge√ßen Ay
              </button>
              <button
                type="button"
                className="btn-quick-date"
                onClick={() => {
                  const today = getTodayDate()
                  const firstDay = new Date()
                  firstDay.setDate(1)
                  setFormData(prev => ({
                    ...prev,
                    donem_baslangic: firstDay.toISOString().split('T')[0],
                    donem_bitis: today
                  }))
                  toast.info('Bu ay d√∂nemi se√ßildi')
                }}
              >
                üìÖ Bu Ay
              </button>
            </div>
          </div>

          {/* Bakiye Bilgisi */}
          <div className="form-section">
            <h3>3. Bakiye Bilgisi</h3>
            <div className="bakiye-section">
              <div className="form-group">
                <label className="required">Bakiye T√ºr√º</label>
                <div className="radio-group">
                  <label className="radio-label">
                    <input
                      type="radio"
                      name="bakiye_turu"
                      value="alacak"
                      checked={formData.bakiye_turu === 'alacak'}
                      onChange={handleInputChange}
                    />
                    <span className="radio-text">Alacak (M√º≈üterinin Borcu)</span>
                  </label>
                  <label className="radio-label">
                    <input
                      type="radio"
                      name="bakiye_turu"
                      value="borc"
                      checked={formData.bakiye_turu === 'borc'}
                      onChange={handleInputChange}
                    />
                    <span className="radio-text">Bor√ß (Bizim Borcumuz)</span>
                  </label>
                </div>
              </div>
              <div className="form-group">
                <label className="required">Bakiye Tutarƒ± (‚Ç∫)</label>
                <input
                  type="number"
                  name="bakiye"
                  value={formData.bakiye}
                  onChange={handleInputChange}
                  placeholder="0.00"
                  step="0.01"
                  min="0"
                  required
                  className="form-input bakiye-input"
                />
                <small className="bakiye-preview">
                  {formData.bakiye && parseFloat(formData.bakiye) > 0 ? (
                    formData.bakiye_turu === 'alacak' ? (
                      <span className="text-success">
                        ‚úÖ M√º≈üteri {parseFloat(formData.bakiye).toLocaleString('tr-TR', { minimumFractionDigits: 2 })} ‚Ç∫ bor√ßlu
                      </span>
                    ) : (
                      <span className="text-danger">
                        ‚ùå Biz {parseFloat(formData.bakiye).toLocaleString('tr-TR', { minimumFractionDigits: 2 })} ‚Ç∫ bor√ßluyuz
                      </span>
                    )
                  ) : (
                    <span className="text-muted">Bakiye tutarƒ±nƒ± girin</span>
                  )}
                </small>
              </div>
            </div>
          </div>

          {/* A√ßƒ±klama */}
          <div className="form-section">
            <h3>4. A√ßƒ±klama (Opsiyonel)</h3>
            <div className="form-group">
              <label>A√ßƒ±klama</label>
              <textarea
                name="aciklama"
                value={formData.aciklama}
                onChange={handleInputChange}
                placeholder="Mutabakat ile ilgili a√ßƒ±klama..."
                rows={4}
                className="form-textarea"
              />
              <small className="text-muted">
                Bo≈ü bƒ±rakƒ±lƒ±rsa otomatik a√ßƒ±klama olu≈üturulacaktƒ±r
              </small>
            </div>
          </div>

          {/* Bilgilendirme */}
          <div className="info-box">
            <h4>‚ÑπÔ∏è Bilgilendirme</h4>
            <ul>
              <li>Bu mutabakat <strong>TASLAK</strong> olarak olu≈üturulacaktƒ±r</li>
              <li>Mutabakat listesinden <strong>g√∂nder</strong> butonuna tƒ±klayarak m√º≈üteriye e-posta g√∂nderebilirsiniz</li>
              <li>Mutabakat t√ºr√º: <strong>Bakiye Mutabakatƒ±</strong> (Kalemsiz)</li>
              <li>M√º≈üteri, e-posta ile gelen linke tƒ±klayarak mutabakatƒ± onaylayabilir veya reddedebilir</li>
            </ul>
          </div>

          {/* Form Butonlarƒ± */}
          <div className="form-actions">
            <button
              type="button"
              className="btn btn-cancel"
              onClick={handleCancel}
              disabled={createMutation.isPending}
            >
              <FaTimesCircle /> ƒ∞ptal
            </button>
            <button
              type="submit"
              className="btn btn-primary"
              disabled={createMutation.isPending || !formData.receiver_id}
            >
              <FaSave />
              {createMutation.isPending ? 'Olu≈üturuluyor...' : 'Mutabakat Olu≈ütur (Taslak)'}
            </button>
          </div>
        </form>
      </div>
    </div>
  )
}
